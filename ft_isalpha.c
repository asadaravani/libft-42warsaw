#include "libft.h"

//Checks if a character is an alphabetic letter (A-Z or a-z).
int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (c);
	return (0);
}
